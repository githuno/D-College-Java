package java.kadai.se.cat01.sec04_method.no001;

/*
 * ◆メソッドの利用
 * メソッド「showMessage()」をmainメソッドで使用し、
 * コンソールに以下の実行結果を表示させましょう。
 *
 * ===== コンソール 実行結果 =====
 * UseJavaMethod01#showMessage()
 * こんにちは！
 * ======================
 *
 * ※showMessage()メソッドの編集不可
 *
 */
public class JavaMethodBasic01 {

	public static void main(String[] args) {


	}

	public static void showMessage() {
		System.out.println("UseJavaMethod01Sub#showMessage()");
		System.out.println("こんにちは！");
	}

}
