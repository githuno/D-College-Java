package mvc.java.kadai.se.cat01.sec01_variable.no013;

/*
 * ◆変数の利用(演算結果の代入)
 *
 * 変数aと変数bの合計値を変数cに代入し、
 * コンソールに出力するプログラムです
 *
 * 以下実行結果になるように①を補い、
 * プログラムを完成させましょう。
 * ※数値(10 + 25のような)の式を書かないでください。
 *
 * ===== コンソール 実行結果 =====
 * 合計値は35
 * ===============================
 */
public class JavaVariableBasic13 {
	public static void main(String[] args) {

		int a = 10;

		int b = 25;

		int c = a + b ;

		System.out.println("合計値は" + c );

	}

}
