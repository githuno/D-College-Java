package mvc.java.kadai.se.cat01.sec01_variable.no005;

/*
 * ◆変数の宣言・代入
 *
 * 以下実行結果になるように①に
 * 文字列が入る変数「message」を宣言し
 * 「Hello World!」を代入してみましょう
 *
 * ===== コンソール 実行結果 =====
 * Hello World!
 * ===============================
 *
 */
public class JavaVariableBasic05 {

	public static void main(String[] args) {

		String message = "Hello World!";
		/* 別解
		 * String message;
		 * message = "Hello World!";
		 */

		// 以降の処理は変更しないでください。
		System.out.println(message);

	}

}
